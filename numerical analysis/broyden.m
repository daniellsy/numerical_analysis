clear;clc;
x(1)=1;y(1)=1;z(1)=1;N=15;k=1;
u1=[x(1);y(1);z(1)];
F1=[x(1)*y(1)-z(1)^2-1;x(1)*z(1)*y(1)+y(1)^2-x(1)^2-2;
    exp(x(1))+z(1)-exp(y(1))-3];
A1=[y(1) x(1) -2*z(1);y(1)*z(1)-2*x(1) x(1)*z(1)+2*y(1) x(1)*y(1)
    ;exp(x(1)) -exp(y(1)) 1];
H1=inv(A1);
while k<=N
u2=u1-H1*F1;
r=u2-u1;
if abs(u2(1)-u1(1))<=0.5*10^(-5)
    break
end
y=[u2(1)*u2(2)-u2(3)^2-1;u2(1)*u2(2)*u2(3)+u2(2)^2-u2(1)^2-2;exp(u2(1))+u2(3)-exp(u2(2))-3]...
    -[u1(1)*u1(2)-u1(3)^2-1;u1(1)*u1(2)*u1(3)+u1(2)^2-u1(1)^2-2;exp(u1(1))+u1(3)-exp(u1(2))-3];
H2=H1+(r-H1*y)*(r.'*H1)/(r.'*H1*y);
F2=[u2(1)*u2(2)-u2(3)^2-1;u2(1)*u2(2)*u2(3)+u2(2)^2-u2(1)^2-2;exp(u2(1))+u2(3)-exp(u2(2))-3];
k=k+1;
H1=H2;
u1=u2;
F1=F2;
end